public class OperadoresEspeciales {
    public static void main(String[] args) {
        int a=5;
        System.out.println(++a);//6
        System.out.println(a);
        float b= (float) a;
        System.out.println(b);
        float x= 5.25f;
        int xx= (int)x;
        System.out.println((float)xx);
    }
}
